# 1 3 5 7 9     1, 9, 2
# 0 1 2 3 4     0, 4, 1
# 4 3 2 1 0     4, 0, -1

i = 1           # 시작
while i <= 9:   # 종료
    print('hello', i)
    i = i+2     # 증감

i = 0
while i <= 4:
    print(i)
    i += 1

i = 0
while i < 5:
    print(i)
    i += 1

i = 4
while i >= 0:
    print(i)
    i -= 1

i = 4
while i >= 0:
    print(i)
    if i == 2:
        break
    i -= 1
print('-'*50)

