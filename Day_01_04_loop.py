for i in range(5):          # 시작(0), 종료, 증감(1)
    print(i, end=' ')
print()

for i in range(0, 5):       # 시작, 종료, 증감(1)
    print(i, end=' ')
print()

for i in range(0, 5, 1):    # 시작, 종료, 증감
    print(i, end=' ')
print()

